function loadBigTexts(lang) {
    document.querySelectorAll("[data-bigtext]").forEach(section => {
        const key = section.getAttribute("data-bigtext");

        if (bigTexts[key] && bigTexts[key][lang]) {
            section.innerHTML = "";

            bigTexts[key][lang].forEach(paragraph => {
                const p = document.createElement("p");
                p.textContent = paragraph;
                section.appendChild(p);
            });
        }
    });
}

